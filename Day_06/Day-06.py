# String and Type Casting

# String
# A string is a sequence of characters enclosed within single quotes or double quotes.
# Example:
single_string = 'Hello World'
double_string = "Hello World" 
triple_string = """Hello World"""

# Type Casting
# Type casting is a method used to convert a variable from one data type to another data type.
# Example:
# int() - converts any data type into integer type
# float() - converts any data type into float type
# str() - converts any data type into string type
# Example:
a = 10
b = 20.5
c = "30"

print(a)
print(float(a))
print(str(a))

print(b)
print(int(b))
print(str(b))

print(c)
print(int(c))
print(float(c))

